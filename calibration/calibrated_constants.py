# Queue-bottleneck constants (heuristic, không từ data fit)
QUEUE_PENALTY_THRESHOLD = 5.0    # task — dưới ngưỡng: không phạt
QUEUE_PENALTY_SLOPE     = 500.0  # ms/task^1.5 — superlinear ramp
QUEUE_PENALTY_CAP_MS    = 30000.0  # ms — cap để tránh latency phi vật lý


def workload_proxy_ms(cpu_requirement: float, deadline_ms: float) -> float:
    duration_s = max(1.0, min(deadline_ms / 1000.0 * 0.5, 15.0))
    return duration_s * (cpu_requirement / 30.0) * 1000.0


def queue_penalty_ms(queue_length: float,
                     threshold: float = QUEUE_PENALTY_THRESHOLD,
                     slope: float = QUEUE_PENALTY_SLOPE,
                     cap_ms: float = QUEUE_PENALTY_CAP_MS) -> float:
    """
    K8s scheduler bottleneck penalty (heuristic).

    Sublinear lúc đầu (queue 5-7: ~1s), superlinear khi quá tải (queue 15+).
    Mô phỏng hiện tượng: API server queue + image pull bandwidth + kubelet sync
    bị contention khi nhồi nhiều pod cùng lúc trên 1 node.
    """
    if queue_length <= threshold:
        return 0.0
    excess = queue_length - threshold
    penalty = (excess ** 1.5) * slope
    return min(float(penalty), cap_ms)


def predict_total_ms(role: str, cpu_requirement: float,
                     deadline_ms: float, cpu_during_exec: float,
                     queue_length: float = 0.0) -> float:
    """
    Predict total K8s task latency (ms) cho role-fitted constants.

    Parameters
    ----------
    queue_length : số task đang xếp hàng tại node (kể cả task hiện tại).
                   Default 0 → backward-compat. Khi gọi từ env training,
                   nên truyền real queue để capture scheduler bottleneck.

    Heavy-task scaling on edge
    --------------------------
    Edge VMs (k3s on weaker hardware) struggle with compute-intensive tasks.
    α được scale theo task cpu_requirement để mô phỏng:
      cpu=10 → α×1.13   (light task: edge OK)
      cpu=30 → α×1.40   (medium)
      cpu=60 → α×1.80   (heavy: edge gần 2× chậm hơn cloud)
    Cloud không scale (capacity dồi dào).
    """
    ec = EXEC_CALIBRATION.get(role)
    oh = OVERHEAD_CALIBRATION.get(role)
    if ec is None or oh is None:
        return 50.0  # fallback
    wp = workload_proxy_ms(cpu_requirement, deadline_ms)

    # Heavy-task α scaling for edge (capacity bottleneck)
    if role.startswith("edge"):
        alpha_eff = ec['alpha'] * (1.0 + 0.8 * cpu_requirement / 60.0)
    else:
        alpha_eff = ec['alpha']

    exec_pred = alpha_eff * wp * (1 + ec['beta'] * cpu_during_exec / 100)
    queue_pen = queue_penalty_ms(queue_length)
    return (oh['submit_overhead_ms'] + oh['container_startup_ms']
            + exec_pred + oh['poll_overhead_ms'] + queue_pen)
